        <h2>Sobre a Pegada de Carbono</h2>
        <p class="sobre-intro">
            Compreender e reduzir a nossa Pegada de Carbono é essencial para combater as alterações climáticas e
            preservar o planeta para as gerações futuras.
            O Acordo de Paris, assinado pelas Nações Unidas a 12 de Dezembro de 2015, é a referência fundamental para
            todas as políticas globais de redução de emissões. Propõe, em particular, manter o aumento da temperatura
            média global abaixo dos 2 °C em comparação com os níveis pré-industriais, fazendo todos os esforços para
            limitá-lo a 1,5 °C; aumentar a capacidade de adaptação aos efeitos adversos das alterações climáticas e
            promover a resiliência climática; e garantir o financiamento necessário para um desenvolvimento com baixas
            emissões e favorável ao clima.

            É do Acordo de Paris que derivam as políticas à escala continental ou nacional, como o Pacto Ecológico
            Europeu, que visa alcançar a neutralidade carbónica na Europa até 2050 (ou seja, um equilíbrio nulo entre o
            CO₂ emitido e absorvido).
